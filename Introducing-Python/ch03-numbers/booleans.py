# booleans
print("\nboolean booleans:")
print(bool(True))
print(bool(False))

# integers
print("\nboolean integers:")
print(bool(1))
print(bool(0))

# string
print("\nboolean string:")
print(bool(""))
print(bool("name"))

# List
print("\nboolean list:")
print(bool([]))
print(bool([1, 2]))

# tuples
print("\nboolean tuples:")
print(bool((),))
print(bool((1, 2, "hello")))

# dictinaries
print("\nboolean dict:")
print(bool(dict()))
print(bool({"name": "rachmat", "age": 36}))
